# Считываем номер месяца от пользователя и преобразуем его в целое число
month = int(input("Введите номер месяца: "))

# Список времён года, где:
# - индекс 0 -> зима (декабрь, январь, февраль: месяцы 12, 1, 2)
# - индекс 1 -> весна (март, апрель, май: 3, 4, 5)
# - индекс 2 -> лето (июнь, июль, август: 6, 7, 8)
# - индекс 3 -> осень (сентябрь, октябрь, ноябрь: 9, 10, 11)
# - индекс 4 -> снова зима (для декабря, месяц 12 → 12//3 = 4)
seasons = ["зима", "весна", "лето", "осень", "зима"]

# Вычисляем индекс времени года:
# - Для месяцев 1–2: 1//3=0, 2//3=0 → зима
# - Для месяца 3: 3//3=1 → весна
# - и так далее
# - Для месяца 12: 12//3=4 → последний элемент списка ("зима")
print(seasons[month // 3])