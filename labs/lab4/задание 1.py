# Считываем температуру в помещении от пользователя и преобразуем в целое число
temp = int(input("Введите температуру в помещении: "))

# Проверяем: если температура ниже 20 градусов
if temp < 20:
    # выводим сообщение, что кондиционер включён
    print("Кондиционер включен")
else:
    # Иначе (температура 20 или выше) — выводим, что кондиционер выключен
    print("Кондиционер выключен")