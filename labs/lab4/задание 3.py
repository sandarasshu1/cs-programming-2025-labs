def dog_to_human(age):
    return age * 10.5 if age <= 2 else 21 + (age - 2) * 4  # первые 2 года — по 10.5, далее — по 4 года

while True:
    try:
        dog_age = int(input("Введите возраст собаки: "))  # читаем целое
        if 1 <= dog_age <= 22:  # допустимый диапазон (22*4+13 ≈ 101 лет человеку)
            print(dog_to_human(dog_age))  # выводим "человеческий" возраст
            break  # корректный ввод → выходим
        print("Ошибка: возраст должен быть от 1 до 22 лет.")  # вне диапазона
    except:  # любая ошибка (ValueError и др.)
        print("Ошибка: введите целое число.")