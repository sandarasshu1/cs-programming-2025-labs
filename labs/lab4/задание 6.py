year = int(input("Введите год: "))  # Считываем год и преобразуем в целое число

# Проверяем правило високосного года:
# Год високосный, если:
#   (делится на 4 И при этом НЕ делится на 100) 
#   ИЛИ (делится на 400)
if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
    print(f"{year} — високосный год")  # Выводим, что год високосный
else:
    print(f"{year} — невисокосный год")  # Выводим, что год невисокосный